const convertInputData = (data) => {
    const listName = []

    for (const name in data) {
        listName.push(name)
    }

    if (listName[0].toLowerCase() !== 'level') {
        throw new Error("Input data is incorrect")
    }

    const listSlot = listName
        .filter(name => name.toLowerCase().includes('slot') && data[name] === "1")
        .map(name => Number(name.toLowerCase().replace('slot', '')))

    const demonsOfMapLv = listSlot.map((slot, index) => {

        const getValue = (valueTitle) => {
            return index === 0 ? data[valueTitle] : data[`${valueTitle}_${index}`]
        }

        const demonsInfo = {
            data: {
                id: 'Todo', //TODO
                rarity: 'Todo', //TODO
                skillSlot: 1,
                stat: {
                    att: { value: Number(getValue('Att')), unit: null },
                    hp: { value: Number(getValue('HP')), unit: null },
                    def: { value: Number(getValue('Def')), unit: null },
                    crit: { value: Number(getValue('Crit')), unit: 'percent' },
                    avoid: { value: Number(getValue('Avd')), unit: 'percent' },
                    speed: { value: Number(getValue('Spd')), unit: null },
                },
                dragonType: getValue('Type'),
                dragonClass: getValue('Spd'),
                hasBasicAbility: true,
                hasAdvancedAbility: false,
            },
            slotNumber: slot
        }

        return demonsInfo
    })

    return {
        mapLevel: data['Level'],
        demonsOfMap: demonsOfMapLv
    };
}

